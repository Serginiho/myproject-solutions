package com.homecompany.chapter5;

class Dog {
    void bark (char x) {System.out.println("Лай");}
    void bark (byte x) {System.out.println("Завывание");}
    void bark (short x) {System.out.println("Поскуливание");}
    void bark (int x, String s) {System.out.println("Попискивание");}
    void bark (String s, int x) {System.out.println("Гав - Гав");}
    void bark (float x) {System.out.println("Ав - Ав");}
    void bark (double x) {System.out.println("Мяу");}
}
