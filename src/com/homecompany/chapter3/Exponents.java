package com.homecompany.chapter3;

public class Exponents {

    public static void main (String[] args){
        float maxFloatValue = 3.4e+38f;
        float minFloatValue = 1.4e-45f;
        double maxDoubleValue = 1.7e+308;
        double minDoubleValue = 4.9e-324;
        System.out.println("Min to Max float value: " + minFloatValue + " to " + maxFloatValue);
        System.out.println("Min to Max double value: " + minDoubleValue + " to " + maxDoubleValue);
    }
}
